const decode = (message) => {
	const myArr = message.split(" ");
	let outPut = "";
	myArr.map(function (item) {
		let sum = 0;
		for (i in item) {
			sum += parseInt(item[i], 10);
		}
		let message = String.fromCharCode(sum);
		outPut += message;
	});
	console.log(outPut);
};

